class Persona:

    def __init__(self, nombre, edad, altura, sexo):
        self._nombre = nombre
        self._edad = edad
        self._altura = altura
        self._sexo = sexo

    def getNombre():
        return self._nombre

    def getEdad():
        return self._edad

    def getAltura():
        return self._altura

    def getSexo():
        return self._sexo

    def setNombre(nombre):
        self._nombre = nombre

    def setEdad(edad):
        self._edad = edad

    def setAltura(altura):
        self._altura = altura

    def setSexo(sexo):
        self._sexo = sexo
